function toggleInitButton() {
  let buttonText = document.getElementById("initButton").innerHTML;
  if (buttonText == "START") {
    //lattis
    // document.getElementById("checkboxCheats").checked = false;

    document
      .getElementById("initButton")
      .style.setProperty("background-color", "gray");
    document.getElementById("initButton").innerHTML = "Reset";
    //Initialize the game here
    resetVariables();
    updateWord();
    hideScoreDisplay();

    inputFieldEnable();
    enableShowCheats();
  } else if (buttonText == "Reset") {
    setCheats(false);
    disableShowCheats();
    hideScoreDisplay();
    //reset variables here...
    resetVariables();
    setProgressBar(0);
    updateScoreTable();
    clearWord();

    document
      .getElementById("initButton")
      .style.setProperty("background-color", "green");
    document.getElementById("initButton").innerHTML = "START";

    inputFieldDisable();
    disableShowCheats();
    document.getElementById("checkboxCheats").checked = false;
  }
}

