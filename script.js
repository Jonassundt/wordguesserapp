//PROGRESSBAR
function progressBarIncrease() {
  let increase = 100 / gameLength;
  if (progressPercent + increase < 101) {
    progressPercent += increase;
    setTimeout(() => {
      bar.style.setProperty("--progress", `${progressPercent}%`);
    }, 100);
  }
  updateBarColor();
}
function progressBarDecrease() {
  let decrease = 100 / gameLength;
  if (progressPercent - decrease >= 0) {
    progressPercent -= decrease;
    setTimeout(() => {
      bar.style.setProperty("--progress", `${progressPercent}%`);
    }, 100);
  }
  updateBarColor();
}
function setProgressBar(score) {
  if (score <= 100 && score >= 0) {
    setTimeout(() => {
      bar.style.setProperty("--progress", `${score}%`);
    }, 0);
  }
}

function updateBarColor() {
  if (progressPercent < 21) {
    setBarColor("red");
  } else if (progressPercent < 41) {
    setBarColor("orange");
  } else if (progressPercent < 61) {
    setBarColor("yellow");
  } else if (progressPercent < 81) {
    setBarColor("green");
  } else {
    setBarColor("lightgreen");
  }
}
function setBarColor(color) {
  //red, green, yellow
  if (color == "green") {
    bar.style.setProperty(
      "background",
      `linear-gradient(rgb(117, 201, 0), rgb(51, 142, 20),rgb(117, 201, 0))`
    );
    bar.style.setProperty("box-shadow", "0 0 10px 0px green");
  } else if (color == "red") {
    bar.style.setProperty(
      "background",
      `linear-gradient(rgb(233, 5, 1), rgb(142, 22, 20),rgb(233, 5, 1))`
    );
    bar.style.setProperty("box-shadow", "0 0 10px 0px red");
  } else if (color == "yellow") {
    //   background: linear-gradient(gold, #c85, gold);
    bar.style.setProperty("background", `linear-gradient(gold, #c85, gold)`);
    bar.style.setProperty("box-shadow", "0 0 10px 0px orange");
  } else if (color == "orange") {
    //   background: linear-gradient(gold, #c85, gold);
    bar.style.setProperty(
      "background",
      `linear-gradient(orange, #c85, orange)`
    );
    bar.style.setProperty("box-shadow", "0 0 10px 0px orange");
  } else if (color == "lightgreen") {
    //   background: linear-gradient(gold, #c85, gold);
    bar.style.setProperty(
      "background",
      `linear-gradient(lightgreen, #8BC34A, lightgreen)`
    );
    bar.style.setProperty("box-shadow", "0 0 10px 0px orange");
  } else {
    alert("something went wrong in setBarColor.");
  }
}
