//Enter
function onPressEnter(ele) {
  if (event.key === "Enter") {
    if (document.getElementById("textInput").value == "") {
      return;
    }
    if (checkEqual()) {
      score += 1;
    }
    total += 1;
    progressBarIncrease();

    if (isGameFinished()) {
      updateScoreFinal();
      resetVariables();
      clearWord();

      setProgressBar(0);
      toggleInitButton();
      showScoreDisplay();
    } else {
      updateWord();
    }
    updateScoreTable();
    updateShowCheats();
  }
}

